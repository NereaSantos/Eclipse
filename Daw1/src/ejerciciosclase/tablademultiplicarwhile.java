package ejerciciosclase;
import java.util.*;
public class tablademultiplicarwhile {

	public static void main(String[] args) {
		
		int numero = 0;
		
		System.out.println("Tabla del 4");
		
		while(numero <= 9) {
		
			numero ++;
			
		int multiplicar = numero * 4;
		
		System.out.println("4 * " + numero + " es igual a " + multiplicar);
		
		
}}}
